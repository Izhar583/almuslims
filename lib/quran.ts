export function getSurahSlug(number: number): string {
  if (number < 1 || number > 114) return "";
  return SURAH_SLUGS[number - 1];
}

export function getSurahNumberFromSlug(slug: string): number {
  if (!slug) return 0;
  const cleanSlug = slug.replace(/^\/+|\/+$/g, "");
  const index = SURAH_SLUGS.indexOf(cleanSlug);
  return index !== -1 ? index + 1 : 0;
}
